const esophagusPattern = /\b(?:oesoph\w*|esoph\w*|gerd|gastroesophageal|gastro-oesophageal|reflux|achalasia|barrett\w*|hiat\w*|zenker\w*|boerhaave\w*|dysphagia|odynophagia|nissen\w*|fundoplication|corrosive|caustic|mallory(?:-weiss)?|pharyngeal pouch|antireflux|anti-reflux|lower sphincter|cardiac achalasia|nutcracker)\b/i

function getHoldReasons(record) {
  const choices = Object.entries(record.choices || {})
  const answerKeys = String(record.verified_answer || '')
    .split(/[;,/|]+/)
    .map((answer) => answer.trim())
    .filter(Boolean)
  const reasons = []

  if (choices.length < 2) reasons.push('fewer than two choices')
  if (answerKeys.length !== 1) reasons.push(answerKeys.length ? 'multiple verified answers' : 'missing verified answer')
  if (answerKeys.length === 1 && !Object.prototype.hasOwnProperty.call(record.choices || {}, answerKeys[0])) {
    reasons.push('verified answer does not map to a choice')
  }
  if (record.answer_status === 'unresolved') reasons.push('unresolved answer')
  if (record.answer_status === 'source_only') reasons.push('source-only answer')
  if (record.answer_confidence === 'low') reasons.push('low answer confidence')

  return reasons
}

function getCategory(record) {
  if (!record.in_named_scope) return 'Mixed General Surgery'
  const classificationText = `${record.topic || ''} ${record.stem || ''}`
  return esophagusPattern.test(classificationText) ? 'Esophagus' : 'Stomach & Duodenum'
}

function getBalancedPartSizes(questionCount) {
  const partCount = Math.max(1, Math.round(questionCount / 34))
  const baseSize = Math.floor(questionCount / partCount)
  const remainder = questionCount % partCount
  return Array.from({ length: partCount }, (_, index) => baseSize + (index < remainder ? 1 : 0))
}

function formatPages(pages) {
  const values = Array.isArray(pages) ? pages : []
  if (!values.length) return 'page unavailable'
  return `${values.length === 1 ? 'page' : 'pages'} ${values.join(', ')}`
}

function toWebsiteQuestion(record) {
  const choiceEntries = Object.entries(record.choices)
  const answerIndex = choiceEntries.findIndex(([key]) => key === record.verified_answer)
  const category = getCategory(record)
  const paperTitle = record.paper_title || record.exam_label || 'Past exam'

  if (answerIndex < 0) throw new Error(`Verified answer is not present in choices for ${record.id}`)

  return {
    id: `sur1-past-${record.id.toLowerCase()}`,
    originalId: record.id,
    originalNumber: record.printed_question_number || null,
    questionType: record.type,
    category,
    organ: category,
    topic: record.topic || '',
    paperTitle,
    pdfPages: record.pdf_pages || [],
    question: record.stem,
    choices: choiceEntries.map(([, choice]) => choice),
    answerIndex,
    explanation: record.explanation || '',
    source: `${paperTitle} · ${formatPages(record.pdf_pages)} · ${record.id}`,
    section: `${paperTitle} · ${record.topic || 'General surgery'}`,
    answerStatus: record.answer_status,
    answerConfidence: record.answer_confidence,
    topicTags: [category, record.topic, record.type].filter(Boolean)
  }
}

function buildGeneratedSource(records, sourceHash, heldRecords, sourceRecordCount) {
  const categoryOrder = ['Esophagus', 'Stomach & Duodenum', 'Mixed General Surgery']
  const websiteQuestions = categoryOrder.flatMap((category) => records
    .filter((record) => getCategory(record) === category)
    .map(toWebsiteQuestion))
  const groupDefinitions = categoryOrder.map((label) => {
    const count = websiteQuestions.filter((question) => question.category === label).length
    return {
      id: label.toLowerCase().replace(/&/g, 'and').replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, ''),
      label,
      sizes: getBalancedPartSizes(count)
    }
  })
  const totalPartCount = groupDefinitions.reduce((total, definition) => total + definition.sizes.length, 0)
  const heldSummary = heldRecords.map(({ record, reasons }) => `${record.id}: ${reasons.join(', ')}`)

  return `// Generated by scripts/build-sur1-past-exam-mcqs.mjs.
// Source SHA-256: ${sourceHash}
// Included: ${websiteQuestions.length} answerable questions from ${sourceRecordCount} deduplicated records.
// Held for review (${heldRecords.length}): ${heldSummary.join('; ')}.
(() => {
  const quizzes = window.mcqQuizzes || (window.mcqQuizzes = {})
  const quiz = quizzes["SUR 401-1 MCQs"] || (quizzes["SUR 401-1 MCQs"] = { alwaysShowSourcePicker: true, sources: [] })
  quiz.alwaysShowSourcePicker = true

  const source = ${JSON.stringify({
    id: 'past-exams-esophagus-stomach',
    label: 'Past Exam MCQs - Esophagus & Stomach',
    description: `Verified past papers · 3 groups · ${totalPartCount} short parts`,
    shuffleQuestions: false,
    shuffleOptions: false,
    mcqs: websiteQuestions
  }, null, 2)}
  const groupDefinitions = ${JSON.stringify(groupDefinitions, null, 2)}

  const summarizePapers = (partQuestions) => {
    const papers = [...new Set(partQuestions.map((question) => question.paperTitle).filter(Boolean))]
    if (!papers.length) return 'Past-exam questions'
    if (papers.length === 1) return papers[0]
    return \`${'${papers[0]}'} → ${'${papers[papers.length - 1]}'}\`
  }

  source.collection = {
    prompt: 'Choose a topic group or a revision mode.',
    groupNoun: 'group',
    groupEyebrow: 'Past-exam bank',
    mixedMeta: 'Random questions from all three past-exam groups.',
    mixedSizes: [
      { id: 'quick-20', label: 'Quick 20', size: 20, description: 'A short mixed past-exam session.' },
      { id: 'standard-30', label: 'Standard 30', size: 30, description: 'Balanced past-exam practice.' },
      { id: 'exam-50', label: 'Exam 50', size: 50, description: 'A longer mixed past-exam session.' }
    ],
    wrongReviewId: 'sur1-past-exams-wrong-review',
    groups: groupDefinitions.map((definition) => {
      const groupQuestions = source.mcqs.filter((question) => question.category === definition.label)
      let offset = 0
      const parts = definition.sizes.map((size, index) => {
        const start = offset + 1
        const end = offset + size
        const partQuestions = groupQuestions.slice(offset, end)
        offset = end
        return {
          id: \`sur1-past-${'${definition.id}'}-p${'${String(index + 1).padStart(2, "0")}'}\`,
          label: \`Part ${'${index + 1}'} · Q${'${start}'}–${'${end}'}\`,
          description: summarizePapers(partQuestions),
          range: \`Q${'${start}'}–${'${end}'}\`,
          questionStart: start,
          questionEnd: end,
          parentSourceId: source.id,
          groupId: definition.id,
          groupLabel: definition.label,
          partIndex: index,
          partCount: definition.sizes.length,
          shuffleQuestions: false,
          shuffleOptions: false,
          mcqs: partQuestions
        }
      })
      return {
        id: definition.id,
        label: definition.label,
        questionCount: groupQuestions.length,
        parts
      }
    })
  }

  quiz.sources = (quiz.sources || []).filter((item) => item.id !== source.id)
  quiz.sources.push(source)
})()
`
}

const sourceText = await readFile(sourcePath, 'utf8')
const records = JSON.parse(sourceText)
if (!Array.isArray(records) || records.length !== 555) {
  throw new Error(`Expected 555 deduplicated records, received ${Array.isArray(records) ? records.length : 'non-array input'}`)
}

const heldRecords = records
  .map((record) => ({ record, reasons: getHoldReasons(record) }))
  .filter(({ reasons }) => reasons.length)
const heldIds = heldRecords.map(({ record }) => record.id).sort()
if (JSON.stringify(heldIds) !== JSON.stringify([...expectedHeldIds].sort())) {
  throw new Error(`Held-record audit changed. Expected ${expectedHeldIds.join(', ')}, received ${heldIds.join(', ')}`)
}

const includedRecords = records.filter((record) => !heldIds.includes(record.id))
const sourceHash = createHash('sha256').update(sourceText).digest('hex')
const generatedSource = buildGeneratedSource(includedRecords, sourceHash, heldRecords, records.length)
await Promise.all(targetPaths.map((targetPath) => writeFile(targetPath, generatedSource, 'utf8')))

const categoryCounts = includedRecords.reduce((counts, record) => {
  const category = getCategory(record)
  counts[category] = (counts[category] || 0) + 1
  return counts
}, {})

